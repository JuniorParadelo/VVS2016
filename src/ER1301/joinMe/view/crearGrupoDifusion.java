        if (!usersList.isSelectionEmpty()) usuarios = usersList.getSelectedValuesList();
        
        usuarios.add(Main.logedInUser.getName());
        
        List<GrupoDifusion> listaGruposGrupo = groupList.getSelectedValuesList();

        //TODO Comprobar add Grupo a grupo
        System.out.println("added Group" + nombreGD.getText());
            Main.model.addGroup(nombreGD.getText(), propietarioGD.getText(), usuarios, listaGruposGrupo);

            grupoDifusionForm.main(null);
            
            dispose();
        
