    public crearGrupoDifusion() {
        initComponents();
        setLocationRelativeTo(null);

        nombreGD.setForeground(java.awt.Color.GRAY);
        propietarioGD.setForeground(java.awt.Color.GRAY);
        propietarioGD.setText((Main.logedInUser.getDni()));
        propietarioGD.disable();

        nombreGD.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (nombreGD.getText().equals("Nombre del Grupo")) {
                    nombreGD.setText("");
                    nombreGD.setForeground(java.awt.Color.BLACK);
                }

            }

            @Override
            public void focusLost(FocusEvent e) {
                if (nombreGD.getText().equals("")) {
                    nombreGD.setText("Nombre del Grupo");
                    nombreGD.setForeground(java.awt.Color.GRAY);
                }
            }
        });

        ActionListener volver = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                grupoDifusionForm.main(null);
                dispose();
            }
        };
        
        volverButton.addActionListener(volver);
        
        reloadUserList();
        reloadGroupList();
    }

